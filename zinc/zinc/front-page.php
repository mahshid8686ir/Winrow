document.addEventListener("DOMContentLoaded", function () {
  const cards = document.querySelectorAll("#supporters-wrapper .supporter-card");
  const positions = ["center", "top", "right", "bottom", "left"];
  let current = 0;

  function updateCards() {
    cards.forEach((card, index) => {
      card.classList.remove("center", "top", "right", "bottom", "left");
      const posIndex = (index - current + cards.length) % cards.length;
      card.classList.add(positions[posIndex]);
    });
  }

  updateCards();

  setInterval(() => {
    current = (current + 1) % cards.length;
    updateCards();
  }, 4000);
});
